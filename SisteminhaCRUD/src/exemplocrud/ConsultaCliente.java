        String nome,rua,cidade,estado;
        int tel1, tel2;
        
        if(campoNome.getText().equals("") ||
                campoRua.getText().equals("") ||
                campoCidade.getText().equals("") ||
                campoEstado.getText().equals("") ||
                campoTelefone1.getText().equals("") ||
                campoTelefone2.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        }else{
            
            nome = campoNome.getText();
            rua = campoRua.getText();
            cidade = campoCidade.getText();
            estado = campoEstado.getText();
            tel1 = Integer.parseInt(campoTelefone1.getText());
            tel2 = Integer.parseInt(campoTelefone2.getText());
            
            Insert i = new Insert(nome,rua,cidade,estado,tel1,tel2);
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
            campoNome.setText("");
            campoRua.setText("");
            campoCidade.setText("");
            campoEstado.setText("");
            campoTelefone1.setText("");
            campoTelefone2.setText("");
        }
